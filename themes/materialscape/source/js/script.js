  function checkEmail() {
    var $f = $('#Field2');
    if ($f.val().length >= 4 && !$f.hasClass('invalid') && $f.val().indexOf('@') > 0 && $f.val().indexOf('.') > 0 && $f.val().indexOf('.') != $f.val().length - 1) {
      return true;
    }
    return false;
  }
